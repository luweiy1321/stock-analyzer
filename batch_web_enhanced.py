# 按交易所、板块和行业分组
@st.cache_data(ttl=3600)  # 缓存1小时
def get_all_stocks_grouped():
    """
    获取所有A股股票，按交易所、板块和行业分组

    Returns:
        分组后的股票数据 {交易所: {板块: {行业: {code: (name, industry)}}}
    """
    grouped = {
        '上交所': {
            '主板': {},
            '科创板': {}
        },
        '深交所': {
            '主板': {},
            '创业板': {},
            '中小板': {}
        }
    }

    # 分类股票
    for code, (name, industry) in STOCK_POOL.items():
        # 判断交易所和板块
        if code.startswith('600'):  # 上交所主板
            market = grouped['上交所']['主板']
        elif code.startswith('688'):  # 上交所科创板
            market = grouped['上交所']['科创板']
        elif code.startswith('000') or code.startswith('001'):  # 深交所主板
            market = grouped['深交所']['主板']
        elif code.startswith('300'):  # 深交所创业板
            market = grouped['深交所']['创业板']
        elif code.startswith('002'):  # 深交所中小板
            market = grouped['深交所']['中小板']
        else:
            continue

        # 按行业分类
        if industry not in market:
            market[industry] = {}
        market[industry][code] = (name, industry)

    return grouped


